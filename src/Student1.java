
public class Student1 {

private String name;
private String email;
public Student1(String name, String email) {
	
	this.name = name;
	this.email = email;
}


public Student1(){
	
}


public String getName() {
	return name;
}


public void setName(String name) {
	this.name = name;
}


public String getEmail() {
	return email;
}


public void setEmail(String email) {
	this.email = email;
}


}
